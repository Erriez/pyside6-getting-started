from PySide6.QtWidgets import QApplication, QWidget, QGridLayout, QLabel, QLineEdit, QTextEdit
import sys


class Window(QWidget):
    def __init__(self):
        super().__init__()

        self.resize(300, 250)
        self.setWindowTitle('Review')

        # Create labels
        title = QLabel('Title')
        author = QLabel('Author')
        review = QLabel('Review')

        # Create line edits
        title_edit = QLineEdit()
        author_edit = QLineEdit()
        review_edit = QTextEdit()

        # Create grid layout
        grid = QGridLayout()
        grid.setSpacing(10)

        # Add widgets to grid layout
        grid.addWidget(title, 1, 0)
        grid.addWidget(title_edit, 1, 1)

        grid.addWidget(author, 2, 0)
        grid.addWidget(author_edit, 2, 1)

        grid.addWidget(review, 3, 0)
        grid.addWidget(review_edit, 3, 1, 5, 1)

        # Add grid layout to window
        self.setLayout(grid)


def main():
    app = QApplication(sys.argv)
    window = Window()
    window.show()
    sys.exit(app.exec())


if __name__ == '__main__':
    main()
